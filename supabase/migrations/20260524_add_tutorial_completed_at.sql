-- =========================================================================
-- 20260524_add_tutorial_completed_at.sql
--
-- users.tutorial_completed_at カラムを追加。チュートリアル（ダッシュボード
-- バナーから起動するウォークスルー）を 1 回完了 or スキップしたら NOW() を
-- 入れて、それ以降はバナーを表示しない判定に使う。
--
-- NULL = まだチュートリアルに触れていない（バナー表示対象）
-- 値あり = 既に 1 回触れた（バナー非表示）
-- =========================================================================

ALTER TABLE public.users
  ADD COLUMN IF NOT EXISTS tutorial_completed_at TIMESTAMPTZ;
